/**
 * Nível de log.
 */
export enum Level {
    /**
     * Informações detalahadas sobre o funcionamento do sistema.
     */
    Verbose,

    /**
     * Informações de depuração.
     */
    Debug,

    /**
     * Informações em geral.
     */
    Information,

    /**
     * Pontos de atenção.
     */
    Warning,

    /**
     * Erros de sistema.
     */
    Error
}
